def add(a, b):
    """
    Add two numbers together.

    :param a: First number
    :param b: Second number
    :return: Sum of a and b
    """
    return a + b

def multiply(a, b):
    """
    Multiply two numbers together

    Args:
        a (int): First number
        b (int): Seconds number

    Returns:
        (int): Product of a and b
    """
    return a * b
